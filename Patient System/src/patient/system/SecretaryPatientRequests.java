    private void Save(boolean shouldSave)
    {
        if(shouldSave == true)
        {
            ArrayList<User> allUsers = UserManagement.getUsers();
            allUsers.add(potentialPatients.get(cmbPatient.getSelectedIndex()));
            UserManagement.saveNewUsers(allUsers);
        }
        
        potentialPatients.remove(cmbPatient.getSelectedIndex());
        PoyentialPatientManagement.saveNewPotentialPatient(potentialPatients);
    }
    
