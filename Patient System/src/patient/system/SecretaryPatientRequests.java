    private void cmbPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPatientActionPerformed
        int currentIndex = cmbPatient.getSelectedIndex();
        Patient currentPatient = (Patient) potentialPatients.get(currentIndex);
        txtAddress.setText(currentPatient.getAddress());
        txtGender.setText(currentPatient.getGender());
        int age = currentPatient.getAge();
        txtAge.setText(String.valueOf(age)); 
    }//GEN-LAST:event_cmbPatientActionPerformed

